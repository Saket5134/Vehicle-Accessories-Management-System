    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        model = (DefaultTableModel) jTable1.getModel();
        rowIndex = jTable1.getSelectedRow();

        int id = Integer.parseInt(model.getValueAt(rowIndex, 0).toString());
        String receivedDate = df.format(date);
        String status = "Received";
        purchase.setDateStatus(id, receivedDate, status);
        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Purchase ID", "User ID", "User Name", "User Mobile", "Accessory Id",
            "Accessory Name", "Quantity", "Price", "Total", "Purchased Date", "Address", "Received Date", "Supplier Name", "Status"}));
        purchase.getOnTheWayAccessoriesData(jTable1, jTextField1.getText(), supplierName);
        statistics.supplier(supplier.getSupplierName(SupplierDashboard.supplierEmail.getText()));
    }//GEN-LAST:event_jTable1MouseClicked

