    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField2.getText());
            String aname = jTextField3.getText();
            String cat = jComboBox1.getSelectedItem().toString();
            int qty = Integer.parseInt(jTextField6.getText());
            if (isNumeric(jTextField4.getText())) {
                double price = Double.parseDouble(jTextField4.getText());
                if (!accessory.isIDExist(id)) {
                    if (!accessory.isaccessCatExist(aname, cat)) {
                        accessory.insert(id, aname, cat, qty, price);
                        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Accessory ID", "Accessory Name", "Category Name", "Quantity", "Price"}));
                        accessory.getAccessoriesData(jTable1, "");
                        clear();
                    } else {
                        JOptionPane.showMessageDialog(this, "Accessory And Category Already Exists", "Warning", 2);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Accessory Id Already Exists", "Warning", 2);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

